import mongoose from "mongoose";

const instituteSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    address: {
        type: String,
        required: true,
    },
    city: {
        type: String,
        required: true,
    },
    state: {
        type: String,
        required: true,
    },
    country: {
        type: String,
        required: true,
    },
    contact: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    website: {
        type: String,
        default: "",
    },
    logo: {
        type: String,
        required: true,
    },
    description: {
        type: String,
        required: true,
    },
    userId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: "User",
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
    level: {
        type: String,
        enum: ["primary", "secondary", "higher",""],
        default: "",
     
    },
    isActive: {
        type: Boolean,
        default: true,
    },
});

const Institute = mongoose.model("tbl_institute", instituteSchema);
export default Institute;